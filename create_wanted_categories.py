# ─── HELPERS ─────────────────────────────────────────────────

def safe_save(page, text, summary):
    """Attempt Page.save but gracefully back off on edit-conflict or if
    the page vanished (was deleted) before we got to save."""
    if not page.exists:
        # Category doesn't exist, try to create it
        try:
            page.save(text, summary=summary)
            return True
        except Exception as e:
            print(f"   ! could not create page – {e}")
            return False

    # Nothing to do if text hasn't changed
    try:
        current = page.text()
    except Exception:
        current = None
    if current is not None and current.rstrip() == text.rstrip():
        return False

    try:
        page.save(text, summary=summary)
        return True
    except mwclient.errors.EditError as e:
        if getattr(e, "code", "") == "editconflict":
            print(f"   ! edit conflict on [[{page.name}]] – skipping")
            return False
        raise
    except mwclient.errors.APIError as e:
        if e.code == "editconflict":
            print(f"   ! edit conflict on [[{page.name}]] – skipping")
            return False
        raise
    except Exception as e:
        print(f"   ! Save failed on [[{page.name}]] – {e}")
        return False


def create_category(category_name):
    """Create a category page with standardized content."""
    try:
        page = site.pages[f'Category:{category_name}']

        # Create the category content
        content = f"[[Category:Categories made on November 5, 2025]]\n"

        # Save the page
        if safe_save(page, content, "Bot: create wanted category"):
            print(f"   • created [[Category:{category_name}]]")
            return True
        else:
            return False

    except Exception as e:
        print(f"   ! error creating [[Category:{category_name}]] – {e}")
        return False


def main():
    """Create all wanted categories."""

    print(f"Creating {len(CATEGORIES)} categories...\n")

    created_count = 0
    failed_count = 0

    for idx, category_name in enumerate(CATEGORIES, 1):
        try:
            print(f"{idx}. [[Category:{category_name}]]")
            if create_category(category_name):
                created_count += 1
            else:
                failed_count += 1
        except Exception as e:
            try:
                print(f"{idx}. [[Category:{category_name}]] – ERROR: {e}")
            except UnicodeEncodeError:
                print(f"{idx}. [category] – ERROR: {str(e)}")
            failed_count += 1

        # Rate limiting to be kind to the server
        time.sleep(0.5)

    print(f"\nDone!")
    print(f"  Created {created_count} categories")
    print(f"  Failed {failed_count} categories")


if __name__ == "__main__":
    main()
